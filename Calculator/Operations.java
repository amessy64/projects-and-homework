public class Operations {
	
	private double result;
	public double add(double x, double y) {
		result = x + y;
		return result;
	}
	
	public double substract(double x, double y) {
		result = x - y;
		return result;
	}
	
	public double multiply(double x, double y) {
		result = x*y;
		return result;	
	}
	public double divide(double x, double y) {
		result = x/y;
		return result;	
	}
}